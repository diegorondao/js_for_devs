// Math.min mostra qual o menor valor e Math.max mostra qual é o maior valor.
console.log('Métodos estáticos min e max do objeto Math')
var n1 = Math.min(1,2,3,4)
var n2 = Math.max(1,2,3,4)
console.log('Valor mínimo entre 1 2 3 4 é =',n1)
console.log('Valor máximo entre 1 2 3 4 é =',n2)
console.log('====================================================')

// Math.round arredonda um valor.
console.log('Método estático round cuja função é arredondar valores para cima.')
var n3 = Math.round(55.49)
console.log('55.49 =', n3)

n3 = Math.round(55.55)
console.log('55.55 =', n3)
console.log('====================================================')

// Math.floor arredonda um valor para baixo.
console.log('Método estático floor cuja função é arredondar valores para baixo.')
var n4 = Math.floor(55.99)
console.log('55.99 =', n4)
console.log('====================================================')


// Math.ceil também arredonda valores para cima.
var n5 = Math.ceil(55.01)
console.log('55.01 =', n5)
console.log('====================================================')


// Math.pow retorna a potência.
var n6 = Math.pow(2,10)
console.log('2 elevado a 10 =', n6)
console.log('====================================================')


// Math.sqrt mostra a raiz quadrada de um número.
var n7 = Math.sqrt(9)
console.log('Raiz quadrada de 9 =', n7)
console.log('====================================================')


// Math.cbrt mostra a raiz cubica de um número.
var n8 = Math.cbrt(8)
console.log('Raiz cubica de 8 =', n8)
console.log('====================================================')

// Math.random() mostra números aleatórios que vão de 0 até 99 ou seja 0.99
var n9 = Math.random()
console.log(n9)
console.log('====================================================')

var n10  = Math.PI.toFixed(2)
console.log('Valor de PI arredondado é =',n10)


